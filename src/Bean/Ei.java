package Bean;

public class Ei {
	private String id;   //数据表中的索引
	private String pid;   //平台编号
	private String name;  //该经济行业的名称
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
